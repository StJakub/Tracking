% --- Executes on button press in wzorzec.
function wzorzec_Callback(hObject, eventdata, handles)
global pattern
pattern_choice = uigetfile('*.*', 'Wybierz wzorzec');
pattern = imread(pattern_choice);
[d1 d2 d3] = size(pattern);
if d3 ~= 1
   pattern = rgb2gray(pattern);
   pattern = im2double(pattern);
end




